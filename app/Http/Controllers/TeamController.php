     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $team = Team::find($id); 

        $team->name = $request->name; 
        $team->project_id=$request->project_id;
        //reration to users
        $team->users()->attach($request->user_id);

        $team->save();
    }

